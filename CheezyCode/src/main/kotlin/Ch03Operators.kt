fun main() {
    var i = 13
    var j = 2
    println(i + j)
    println(i - j)
    println(i * j)
    println(i.toFloat() / j)
    println(i % j)

    println(i < j)
    println(i == j)
    println(i > j)
    println(i >= j)
    println(i <= j)
    println(i != j)

    println()
    var k = 10
    k++
    println(k)
    k--
    println(k)
    println(k++)
    println(++k)
}